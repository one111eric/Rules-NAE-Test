package impl;

public class Code {
	private String type;
	private String zone;

	public Code() {
		this.zone="";
		this.type="";
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getZone() {
		return zone;
	}

	public void setZone(String zone) {
		this.zone = zone;
	}
}
